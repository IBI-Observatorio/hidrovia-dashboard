export function diaDoAno(dataISO: string): number {
  const [y, m, d] = dataISO.split("-").map(Number);
  const inicio = Date.UTC(y, 0, 1);
  const atual  = Date.UTC(y, m - 1, d);
  return Math.floor((atual - inicio) / 86400000) + 1;
}
